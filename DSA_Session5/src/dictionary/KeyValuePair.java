package dictionary;

/**
 * KeyValuePair class for storing key and values
 * 
 * @author Aman Gautam
 * 
 * Dated 8/8/2019
 *
 */
public class KeyValuePair {

	int key;
	String value;
	
	/**
	 * Instantiates a new key value pair.
	 *
	 * @param key the key
	 * @param value the value
	 */
	public KeyValuePair(int key,String value){
		this.key=key;
		this.value = value;
	}
}

